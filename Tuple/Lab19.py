# +Ve Index :   0, 1,  2,  3,  4,  5,  6
# Tuple     :( 10, 20, 30, 40, 50, 60, 70 )
# -Ve Index :  -7, -6, -5, -4, -3, -2, -1

myTuple=(10,20,30,40,50,60,70)
print("[2:5]",myTuple[2:5]) # (30, 40, 50)
print("[2:6]",myTuple[2:6]) # (30, 40, 50, 60)
print("[2:60000]",myTuple[2:60000]) # (30, 40, 50, 60, 70)
print("[1:6:1]",myTuple[1:6:1]) # (20, 30, 40, 50, 60)
print("[1:6:2]",myTuple[1:6:2]) # (20,  40,  60)
print("[2:]",myTuple[2:]) # (30, 40, 50, 60, 70)
print("[:7]",myTuple[:7]) # (10, 20, 30, 40, 50, 60, 70)
print("[0:0]",myTuple[0:0]) # ()


