# +Ve Index :   0, 1,  2,  3,  4,  5,  6
# Tuple     :( 10, 20, 30, 40, 50, 60, 70 )
# -Ve Index :  -7, -6, -5, -4, -3, -2, -1

myTuple=(10,20,30,40,50,60,70)
print("[-2:-5]",myTuple[-2:-5]) # ()
print("[-2:5]",myTuple[-2:5]) # ()
print("[-5:-2]",myTuple[-5:-2]) # (30, 40, 50)

print("[-5:-2:2]",myTuple[-5:-2:2]) # (30, 50)
print("[-5:-2:-1]",myTuple[-5:-2:-1]) #  ()

